using Pdf.Documents.Security;
using System.Text;
using Xunit;

namespace PicoPDF.Test;

public class Aes256HandlerTest
{
    [Fact]
    public void ComputingHash_Algorithm2B()
    {
        var hash = new byte[32];
        Aes256Handler.ComputingHash_Algorithm2B(Encoding.UTF8.GetBytes("xyz987"), [0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07], [], hash);
        Assert.Equal(
            hash,
            [
                0xca, 0x88, 0xc4, 0xf0, 0xb1, 0xca, 0x5c, 0xbf, 0x2f, 0x62, 0x45, 0xd3, 0x0e, 0x1d, 0x85, 0xdb,
                0x52, 0x9d, 0x6e, 0xaa, 0x16, 0xbf, 0x18, 0x33, 0xdc, 0xa2, 0x68, 0x32, 0xdc, 0xef, 0x68, 0xbf,
            ]);
    }
}
